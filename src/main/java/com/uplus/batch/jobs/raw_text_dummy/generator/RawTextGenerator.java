    private String pickAndSerialize(List<List<Turn>> variants, Random random) {
        List<Turn> selected = variants.get(random.nextInt(variants.size()));
        return serialize(selected);
    }

    private String serialize(List<Turn> turns) {
        StringBuilder sb = new StringBuilder("[");
        for (int i = 0; i < turns.size(); i++) {
            if (i > 0) sb.append(",");
            Turn t = turns.get(i);
            sb.append("{\"speaker\":\"")
              .append(escape(t.speaker()))
              .append("\",\"text\":\"")
              .append(escape(t.text()))
              .append("\"}");
        }
        return sb.append("]").toString();
    }

    private String escape(String s) {
        if (s == null) return "";
        return s.replace("\\", "\\\\")
                .replace("\"", "\\\"")
                .replace("\n", "\\n")
                .replace("\r", "\\r");
    }
